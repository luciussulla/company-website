<h3>Omówienie frazy &#8211; &#8220;Orzekam jak w sentencji&#8221; </h3>
<p>&#8220;Po rozpatrzeniu wszystkich okoliczności faktycznych i prawnych, orzekłem jak w sentencji decyzji.&#8221;</p>
<p>(Decyzja wydana przez prezydenta miasta) &#8211; Having considered, &#8230; I ruled/found/decreed as in the holding </p>
<p>1. &#8220;Holding&#8221; to część dyspozytywna. Kiedy było potrzebne bardzo literalne tłumaczenie polskiego aktu, np. do przedstawienia w sądzie za granicą, tłumaczyłem &#8220;as in the dispositive part&#8221; albo nawet &#8220;as in the sentence&#8221; (&#8220;sentence&#8221; nie musi być wyrokiem karnym, oznacza tyle, co &#8220;pronouncement&#8221;). Jest to kwestia stylu, ale tutaj wolałbym &#8220;ruled&#8221; potem inne czasowniki &#8211; &#8220;found&#8221;, &#8220;decreed&#8221; itp.</p>
<p>Therefore the court had to decide as in the sentencing part of the judgement.</p>
