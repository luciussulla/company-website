		<h2>Ile jest rodzajów tłumaczeń?</h2>
		<p>
		Rodzaj tłumaczenia zależeć będzie od bezpośredniości działania, czyli będziemy mówić, albo o "tłumaczeniu symultanicznym" lub "tłumaczeniu konsekutywnym". W tłumaczeniu symultanicznym tłumacz wysyła nieprzerwanie wiadomość wypowiadaną przez mówce. Taka wiadomość dociera do odbiorców docelowych w słuchawkach. Ten typ tłumaczenia jest bardzo powszechny w spotkaniach i wydarzeniach z udziałem kilku osób.
		W "tłumaczeniu konsekutywnym" tłumacz siedzi lub stoi obok osoby mówiącej w języku źródłowym, która robi pauzy w swoim wystąpieniu, aby tłumacz mógł wygenerować swoją wiadomość w języku docelowym. W takim przypadku nie ma potrzeby angażowania kabin ani wyposażenia technicznego. Innym rodzajem interpretacji jest tak zwana "szeptanka". Technika ta jest stosowana w grupach, w których większość dobrze włada językiem obcym, z wyjątkiem jednej lub dwóch osób, które tego nie potrafią. Na przykład cywilna ceremonia ślubna, w której jedna z umawiających się stron jest cudzoziemką. Tłumacz siedzi za tą osobą i szepcze rozmówcy przetłumaczoną treść.</p>

