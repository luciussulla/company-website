        <h1>Co to jest tłumaczenie symultaniczne?</h1>

		<p> <strong>"Tłumaczenie symultaniczne" lub "kabinowe" to proces przekazywania wiadomości ustnej z jednego języka na drugi
		(języki te są nazywane "językiem źródłowym" i "językiem docelowym")</strong>. Takie tłumaczenie odbywa się to natychmiastowo, co oznacza, że tłumacz przekazuje wiadomość do języka docelowego, kiedy mówca kontynuuje generowanie treści w języku źródłowym. Słowa "tłumaczenie symultaniczne" i "tłumaczenie kabinowe" są synonimami.</p>


