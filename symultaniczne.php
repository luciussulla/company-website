		Najczęściej mówiąc o tłumaczeniu mamy na myśli tłumaczenie pisane, odtwarzamy wówczas treść określonego dokumentu na inny język. Aby zostać tłumaczem, niezbędne jest posiadanie pełnej znajomości obu języków w których pracujemy, aby uniknąć błędów gramatycznych, błędów pisowni i zniekształceń znaczenia. Ogólnie rzecz biorąc, tłumacz ma wystarczająco dużo czasu na wykonanie badań tematu i leksyki użytej w dokumencie.
		<strong>Jeśli chodzi o tłumaczenie ustne lub symultaniczne, nie ma czasu na badania, ponieważ treść jest przekazywana ustnie i natychmiast. </strong> Interpretacja wymaga ponadto pełnej znajomości dwóch umiejętności językowych: słuchania i mówienia (w połączeniu z dobrym i wyraźnym tonem głosu).
