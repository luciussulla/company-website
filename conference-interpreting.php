<h1>What is conference interpreting</h1>
<p>Interpretation is in itself a difficult profession. A translator or interpreter has to be knowledgeable about many fields of science and knowledge, which guarantees him proficiency in his job and accuracy. This is even more relevant when one thinks about oral interpretation especially simultaneous or consecutive. Both require, not only expert knowledge of the subject one deals with but also proper training and experience. </p>
<h2>What is simultaneous interpretation</h2>
                        
<p>Translators in action</p>
<p>Simultaneous interpretation is a form of oral translation that is particularly useful in multi-lingual conferences. It has been gradually ousting consecutive interpretation due to the fact that it allows direct translation of the speakers message. The interpreter sitting in a booth is receiving the signal from the speaker&#8217;s microphone and thus is able to instantaneously translate the message to the guests located in the room. This form of translation is usually more costly to the organizers of the conference due to the fact that it is necessary to hire a technician and rent the booth, transmitters and the entire equipment. This however is a necessity since the only alternative &#8211; consecutive interpretation would result in serious lags and would cause unnecessary delays in the conference itself. </p>
<h3>What type of translation do I need?</h3>
<p>Well, to be honest, if you are organizing a serious conference you should definitely go with the simultaneous translation since otherwise you will prolong the whole event and cause unnecessary lags. This is because consecutive translators have to wait until the speaker finishes talking and then he repeats what was being said. This naturally takes time, which simultaneous interpreters save by translating instantaneously</p>
<h3>How to become an interpreter?</h3>
<p>Once you realize that you have to instantaneously interpret what is being said, you realize that it is not an easy thing to do. In fact, it is very difficult and demanding. It requires long hours spent in the booth i.e. a lot of practice. In Europe there are numerous courses organized at universities that are specially aimed to train students who would later seek employment in EU institutions. These courses to be valid have to have EU accreditation. The courses train interpreters in 23 official EU languages. After graduating from such a course, the student has to pass a series of qualification exams, which allows him to finally find steady employment in the European Union. European Parliament happens to be the biggest employer of oral translators on the planet. Another prominent institution is the United Nations with their numerous institutions and organizations and its worldwide presence.</p>

